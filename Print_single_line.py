
print(5, end= "")
print(" + ", end= "")
print(8, end= "")
print(" - ", end= "")
print(4, end= "")
print(" = ", end= "")
print(5 + 8 - 4)

# 5 + 8 - 4 = 9

# Each print command usually prints out a line of its own, complete with a change of line at the end. 
# However, if the print command is given an additional argument end = "", it will not print a line change.